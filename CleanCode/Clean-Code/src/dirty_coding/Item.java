package dirty_coding;

public class Item {
	private String desc;
	private double prce;
	private int qtt;
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public double getPrce() {
		return prce;
	}
	public void setPrce(double prce) {
		this.prce = prce;
	}
	public int getQtt() {
		return qtt;
	}
	public void setQtt(int qtt) {
		this.qtt = qtt;
	}
	
}
